//* Comparison Operators
//* > Greater than
//* < Less than
//* >= Greater than or equal to
//* <= Less than or equal to
//* == Equality
//* != Not equal
//* === Strict equality
//* !== Strict non-equality

//* Comparison Examples
console.log(10 > 4);
console.log(0.2 > 0.3);
console.log(-10 < 0);
console.log(50.5 < 5);
console.log(0.5 <= 0.5);
console.log(99 >= 4);
console.log(99 >= 99);
console.log('a' < 'b');
console.log('A' > 'a');

//* Double Equals
console.log(5 == 5);
console.log(0 == false);
console.log(null == undefined);

//* Triple Equals
console.log(2 === '2');
console.log(false === 0);

//* Same Applies for != and !==
console.log(10 != '10');
console.log(0 !== '10');
