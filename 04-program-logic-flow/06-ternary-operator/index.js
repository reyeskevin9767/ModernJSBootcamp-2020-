//* Normal If/else Conditional
let num = 7;

//* Checks if num is 7
if (num === 7) {
  console.log('lucky');
} else {
  console.log('bad');
}

//* Ternary Operator
//* /Check if num is 4
num === 4 ? console.log('lucky') : console.log('bad');

let weekendDay = 'Friday';

//* Check if weekendDay is Saturday or Sunday
weekendDay === 'Saturday' || weekendDay === 'Sunday'
  ? console.log('Its a the weekend')
  : console.log('No, its friday');
