//* UpperCase
let newMsg = 'I am king';
let yellMsg = newMsg.toUpperCase();

console.log(yellMsg); // I AM KING

//* LowerCase
let angry = 'LeAve mE aLoNe';
let newAngry = angry.toLowerCase();

console.log(newAngry); // leave me alone

let game = 'gameover';
let gameOver = game.toUpperCase();

console.log(gameOver); // GAMEOVER

//* Trim
let greeting = '          leave me alone plz       ';
let newGreeting = greeting.trim();

console.log(newGreeting); // leave me alone plz

let goodbye = 'gooooodddddbyyyyeeee     ';
let newGoodBye = goodbye.trim();

console.log(newGoodBye); // gooooodddddbyyyyeeee

let death = '   deathfromabove';
let highDeath = death.trim();

console.log(highDeath); // deathfromabove
