//* Variables Can Change Types
let numDonuts = 12;
console.log(numDonuts); // Number

numDonuts = false;
console.log(numDonuts); // Now A Boolean

numDonuts = 12321455;
console.log(numDonuts); // Back to Number

let numOfCookies = 10;
console.log(numOfCookies); // Number

numOfCookies = true;
console.log(numOfCookies); // Now A Boolean

numOfCookies = 'Free Cookies';
console.log(numOfCookies); // Now A String
