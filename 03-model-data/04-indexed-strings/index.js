//* Indexed Strings
let firstName = 'Ziggy';

console.log(firstName.length); // 5

console.log(firstName[0]); // Z
console.log(firstName[3]); // g
console.log(firstName[5]); //undefined

let lastName = 'Wingman';

console.log(lastName.length); // 5

console.log(lastName[2]); // n
console.log(lastName[4]); // m
console.log(lastName[-1]); // undefined
