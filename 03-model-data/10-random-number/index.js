//* Random Number Generator
let randNumber = Math.floor(Math.random() * 10) + 1;
console.log(randNumber); // Gives back a whole number between 1 and 10

let randNumber2 = Math.floor(Math.random() * 100);
console.log(randNumber2); // Gives back a whole number between 0 and 100

let randNumber3 = Math.floor(Math.random() * 20) + 2;
console.log(randNumber3); // Gives back a whole number between 2 and 20
