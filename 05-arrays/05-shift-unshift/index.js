let dishesToDo = ['large platter'];

//* Unshift - Adds To The Start
dishesToDo.unshift('plate');
console.log(dishesToDo);

dishesToDo.unshift('cereal bowl');
console.log(dishesToDo);

dishesToDo.unshift('spoon');
console.log(dishesToDo);

//* Shift - Remove From The Start
dishesToDo.shift();
console.log(dishesToDo);

dishesToDo.shift();
console.log(dishesToDo);

dishesToDo.shift();
console.log(dishesToDo);
