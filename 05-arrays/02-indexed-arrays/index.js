//* Indexed Arrays
let colors = ['red', 'orange', 'yellow', 'green'];

console.log(colors.length); // 4
console.log(colors[0]); // red
console.log(colors[1]); // orange
console.log(colors[2]); // yellow
console.log(colors[3]); // green
console.log(colors[4]); // undefined

const lottoNums = [32, 43, 54, 21, 54, 21, 2, 5];

console.log(lottoNums.length); // 8
console.log(lottoNums[3]); // 21
console.log(lottoNums[2]); // 54
console.log(lottoNums[6]); // 2
console.log(lottoNums[43]); // green
console.log(lottoNums[-2]); // undefined
