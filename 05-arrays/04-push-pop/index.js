//* Push And Pop
let topSongs = [
  'First Time Ever I Save Your Face',
  'God Only Knows',
  'A Day In The Life',
  'Life On Mars',
];

//* Push - Add To End of Array
topSongs.push('Fortunate Son');
console.log(topSongs);

topSongs.push('Landslide');
console.log(topSongs);

topSongs.push(1234353);
console.log(topSongs);

//* Pop - Removes The Last Element
topSongs.pop();
console.log(topSongs);

topSongs.pop();
console.log(topSongs);

topSongs.pop();
console.log(topSongs);
