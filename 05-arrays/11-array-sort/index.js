//* sort()
let people = ['Mrs. Robsinson', 'Angie', 'Jolene', 'Maggie May', 'Roxanne'];

let sortPeople = people.sort();
console.log(sortPeople); // [ 'Angie', 'Jolene', 'Maggie May', 'Mrs. Robsinson', 'Roxanne' ]

const animals = ['Monkey', 'Lizard', 'Cat', 'Dog', 'Mouse'];

let sortAnimals = animals.sort();
console.log(sortAnimals); // [ 'Cat', 'Dog', 'Lizard', 'Monkey', 'Mouse' ]

const fruits = ['Banana', 'Watermelons', 'Graple', 'Apple'];

let sortFruits = fruits.sort();
console.log(sortFruits); // [ 'Apple', 'Banana', 'Graple', 'Watermelons' ]
